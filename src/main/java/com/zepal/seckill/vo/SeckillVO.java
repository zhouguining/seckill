package com.zepal.seckill.vo;

import java.io.Serializable;


/**
   *  
 * */
public class SeckillVO implements Serializable {

	private static final long serialVersionUID = 1L;

	private long seckillId;
	
	private String name;
	
	private int number;
	
	private String startTime;
	
	private String endTime;
	
	private String createTime;

	public long getSeckillId() {
		return seckillId;
	}

	public void setSeckillId(long seckillId) {
		this.seckillId = seckillId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public SeckillVO() {
		super();
	}

	public SeckillVO(long seckillId, String name, int number, String startTime, String endTime, String createTime) {
		super();
		this.seckillId = seckillId;
		this.name = name;
		this.number = number;
		this.startTime = startTime;
		this.endTime = endTime;
		this.createTime = createTime;
	}

	@Override
	public String toString() {
		return "ListSeckillVO [seckillId=" + seckillId + ", name=" + name + ", number=" + number + ", startTime="
				+ startTime + ", endTime=" + endTime + ", createTime=" + createTime + "]";
	}
	
}
